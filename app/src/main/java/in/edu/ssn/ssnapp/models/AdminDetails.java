package in.edu.ssn.ssnapp.models;

public class AdminDetails {
    String name, email;

    public AdminDetails() {
        this.email = email;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
